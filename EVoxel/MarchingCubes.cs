        private static bool LessThan (Vector3 a_vectorA, Vector3 a_vectorB)
        {
            for (int i = 0; i < 3; ++i)
            {
                if (a_vectorA[i] < a_vectorB[i])
                {
                    return true;
                }
                else if (a_vectorA[i] > a_vectorB[i])
                {
                    return false;
                }
            }

            return false;
        }

        private static Vector3 LinearInterp (Vector4 a_vectorA, Vector4 a_vectorB, float a_value)
        {
            if (LessThan(a_vectorB.Xyz, a_vectorA.Xyz))
            {
                Vector4 temp = a_vectorA;
                a_vectorA = a_vectorB;
                a_vectorB = temp;
            }

            Vector3 p;
            if (Math.Abs(a_vectorA.W - a_vectorB.W) > 0.00001)
            {
                float isoDif = (a_value - a_vectorA.W);
                float surfDir = (a_vectorB.W - a_vectorA.W);

                p = a_vectorA.Xyz + (a_vectorB.Xyz - a_vectorA.Xyz) / surfDir * isoDif;
            }
            else
            {
                p = a_vectorA.Xyz;
            }

            return p;
        }

